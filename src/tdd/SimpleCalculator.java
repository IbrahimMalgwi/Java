package tdd;

public class SimpleCalculator {
    public int add(int number1, int number2) {
        int calculateA = number1 + number2;
        return calculateA;
    }

    public int mult(int number1, int number2) {
        int calculateM = number1 * number2;
        return calculateM;
    }

    public int div(int number1, int number2) {
        int calculateD = number1 / number2;
        return calculateD;
    }

    public int sub(int number1, int number2) {
        int calculateS = number1 - number2;
        return calculateS;
    }
}
